const $editButton = document.getElementById("edit")
let isEditing = false;

const editHandler = () => {
  const url = new URLSearchParams(window.location.search);
  const params = Object.fromEntries(url.entries());
  const title = params.title;
  
  let $body = document.getElementById("body");

  if (!isEditing) {
    const bodyValue = $body.innerHTML;

   
    $editButton.innerHTML = '<svg xmlns="http://www.w3.org/2000/svg" width="1em" height="1em" preserveAspectRatio="xMidYMid meet" viewBox="0 0 24 24"><path fill="currentColor" d="m20.71 9.29l-6-6a1 1 0 0 0-.32-.21A1.09 1.09 0 0 0 14 3H6a3 3 0 0 0-3 3v12a3 3 0 0 0 3 3h12a3 3 0 0 0 3-3v-8a1 1 0 0 0-.29-.71ZM9 5h4v2H9Zm6 14H9v-3a1 1 0 0 1 1-1h4a1 1 0 0 1 1 1Zm4-1a1 1 0 0 1-1 1h-1v-3a3 3 0 0 0-3-3h-4a3 3 0 0 0-3 3v3H6a1 1 0 0 1-1-1V6a1 1 0 0 1 1-1h1v3a1 1 0 0 0 1 1h6a1 1 0 0 0 1-1V6.41l4 4Z"/></svg>'

    $body.outerHTML = `<textarea id="body">${bodyValue}</textarea>`
  } else {
    const bodyValue = $body.value;

    $editButton.innerHTML = '<svg xmlns="http://www.w3.org/2000/svg" width="1em" height="1em" preserveAspectRatio="xMidYMid meet" viewBox="0 0 24 24"><path fill="currentColor" d="M21 12a1 1 0 0 0-1 1v6a1 1 0 0 1-1 1H5a1 1 0 0 1-1-1V5a1 1 0 0 1 1-1h6a1 1 0 0 0 0-2H5a3 3 0 0 0-3 3v14a3 3 0 0 0 3 3h14a3 3 0 0 0 3-3v-6a1 1 0 0 0-1-1Zm-15 .76V17a1 1 0 0 0 1 1h4.24a1 1 0 0 0 .71-.29l6.92-6.93L21.71 8a1 1 0 0 0 0-1.42l-4.24-4.29a1 1 0 0 0-1.42 0l-2.82 2.83l-6.94 6.93a1 1 0 0 0-.29.71Zm10.76-8.35l2.83 2.83l-1.42 1.42l-2.83-2.83ZM8 13.17l5.93-5.93l2.83 2.83L10.83 16H8Z"/></svg>' 

    let notes = localStorage.getItem('notes')
    notes = !notes ? [] : JSON.parse(notes)
    
    const noteToEdit = notes.find(note => note.noteTitle === title);
    noteToEdit.noteBody = bodyValue;

    localStorage.setItem('notes', JSON.stringify(notes))

    $body.outerHTML = `<p id="body">${bodyValue}</p>`
  }
    
  isEditing = !isEditing;
}

$editButton.addEventListener('click', editHandler)